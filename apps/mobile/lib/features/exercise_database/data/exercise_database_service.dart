  List<ExerciseDetail> filterExercises({
    required List<ExerciseDetail> exercises,
    String category = 'All',
    String muscleGroup = 'All',
    String equipment = 'All',
    String difficulty = 'All',
    String search = '',
  }) {
    return exercises.where((e) {
      final matchCat    = category == 'All' || e.category == category;
      final matchMuscle = muscleGroup == 'All' || e.muscleGroup == muscleGroup;
      final matchEquip  = equipment == 'All' || e.equipment == equipment;
      final matchDiff   = difficulty == 'All' || e.difficulty == difficulty;
      final q = search.toLowerCase();
      final matchSearch = q.isEmpty
          || e.name.toLowerCase().contains(q)
          || e.muscleGroup.toLowerCase().contains(q)
          || e.category.toLowerCase().contains(q)
          || e.difficulty.toLowerCase().contains(q)
          || e.equipment.toLowerCase().contains(q)
          || e.tags.any((t) => t.contains(q));
      return matchCat && matchMuscle && matchEquip && matchDiff && matchSearch;
    }).toList();
  }
